use crate::processors::{AggWatch, PumpSwapShredWatch, PumpfunShredWatch};
use axum::{routing::get, Json, Router};
use carbon_core::pipeline::Pipeline;
use carbon_jito_shredstream_grpc_datasource::JitoShredstreamGrpcClient;
use std::env;

#[tokio::main]
async fn main() -> Result<(), Box<dyn std::error::Error + Send + Sync>> {
    init_tracing();

    let shred_url = env::var("SHRED_GRPC_URL").unwrap_or_else(|_| "http://127.0.0.1:9999".into());
    let health_port: u16 = env::var("SHREDS_HEALTH_PORT")
        .ok()
        .and_then(|s| s.parse().ok())
        .unwrap_or(9102);

    log::info!(
        "frankfurt-carbon-shreds starting (shred_grpc={shred_url}, health_port={health_port})"
    );

    // 1. Subsystems — bounded mpsc writer, coordinator flusher, watch list refresh.
    writer::spawn();
    coordinator::spawn_flusher();
    watch_list_sync::spawn();

    // 2. Health endpoint. Bound to localhost only — internal scrape surface.
    spawn_health_server(health_port);

    // 3. Carbon Pipeline — Jito shred datasource + every decoder we care
    //    about. PumpFun + PumpSwap have full extraction; the rest run
    //    behind AggWatch (decoded counts only) until per-aggregator
    //    extraction lands in Stage 3.
    let datasource = JitoShredstreamGrpcClient::new(shred_url);

    let mut pipeline = Pipeline::builder()
        .datasource(datasource)
        // ── Direct trade decoders — full extraction, emit triggers ──
        .instruction(carbon_pumpfun_decoder::PumpfunDecoder, PumpfunShredWatch)
        .instruction(carbon_pump_swap_decoder::PumpSwapDecoder, PumpSwapShredWatch)
        // ── Launchpads — metric-only for now ──
        .instruction(
            carbon_bonkswap_decoder::BonkswapDecoder,
            AggWatch::<carbon_bonkswap_decoder::instructions::BonkswapInstruction>::new("bonkswap"),
        )
        .instruction(
            carbon_moonshot_decoder::MoonshotDecoder,
            AggWatch::<carbon_moonshot_decoder::instructions::MoonshotInstruction>::new("moonshot"),
        )
        .instruction(
            carbon_boop_decoder::BoopDecoder,
            AggWatch::<carbon_boop_decoder::instructions::BoopInstruction>::new("boop"),
        )
        .instruction(
            carbon_heaven_decoder::HeavenDecoder,
            AggWatch::<carbon_heaven_decoder::instructions::HeavenInstruction>::new("heaven"),
        )
        .instruction(
            carbon_gavel_decoder::GavelDecoder,
            AggWatch::<carbon_gavel_decoder::instructions::GavelInstruction>::new("gavel"),
        )
        .instruction(
            carbon_meteora_dbc_decoder::DynamicBondingCurveDecoder,
            AggWatch::<carbon_meteora_dbc_decoder::instructions::DynamicBondingCurveInstruction>::new("meteora_dbc"),
        )
        .instruction(
            carbon_raydium_launchpad_decoder::RaydiumLaunchpadDecoder,
            AggWatch::<carbon_raydium_launchpad_decoder::instructions::RaydiumLaunchpadInstruction>::new("raydium_launchpad"),
        )
        .instruction(
            carbon_onchain_labs_dex_v1_decoder::OnchainLabsDexV1Decoder,
            AggWatch::<carbon_onchain_labs_dex_v1_decoder::instructions::OnchainLabsDexV1Instruction>::new("onchain_labs_dex_v1"),
        )
        .instruction(
            carbon_onchain_labs_dex_v2_decoder::OnchainLabsDexV2Decoder,
            AggWatch::<carbon_onchain_labs_dex_v2_decoder::instructions::OnchainLabsDexV2Instruction>::new("onchain_labs_dex_v2"),
        )
        // ── Native AMMs — metric-only for now ──
        .instruction(
            carbon_raydium_amm_v4_decoder::RaydiumAmmV4Decoder,
            AggWatch::<carbon_raydium_amm_v4_decoder::instructions::RaydiumAmmV4Instruction>::new("raydium_amm_v4"),
        )
        .instruction(
            carbon_raydium_clmm_decoder::RaydiumClmmDecoder,
            AggWatch::<carbon_raydium_clmm_decoder::instructions::RaydiumClmmInstruction>::new("raydium_clmm"),
        )
        .instruction(
            carbon_raydium_cpmm_decoder::RaydiumCpmmDecoder,
            AggWatch::<carbon_raydium_cpmm_decoder::instructions::RaydiumCpmmInstruction>::new("raydium_cpmm"),
        )
        .instruction(
            carbon_raydium_stable_swap_decoder::RaydiumStableSwapAmmDecoder,
            AggWatch::<carbon_raydium_stable_swap_decoder::instructions::RaydiumStableSwapAmmInstruction>::new("raydium_stable_swap"),
        )
        .instruction(
            carbon_meteora_damm_v2_decoder::MeteoraDammV2Decoder,
            AggWatch::<carbon_meteora_damm_v2_decoder::instructions::MeteoraDammV2Instruction>::new("meteora_damm_v2"),
        )
        .instruction(
            carbon_meteora_dlmm_decoder::MeteoraDlmmDecoder,
            AggWatch::<carbon_meteora_dlmm_decoder::instructions::MeteoraDlmmInstruction>::new("meteora_dlmm"),
        )
        .instruction(
            carbon_meteora_pools_decoder::MeteoraPoolsDecoder,
            AggWatch::<carbon_meteora_pools_decoder::instructions::MeteoraPoolsProgramInstruction>::new("meteora_pools"),
        )
        .instruction(
            carbon_meteora_vault_decoder::MeteoraVaultDecoder,
            AggWatch::<carbon_meteora_vault_decoder::instructions::MeteoraVaultInstruction>::new("meteora_vault"),
        )
        .instruction(
            carbon_orca_whirlpool_decoder::OrcaWhirlpoolDecoder,
            AggWatch::<carbon_orca_whirlpool_decoder::instructions::WhirlpoolInstruction>::new("orca_whirlpool"),
        )
        .instruction(
            carbon_lifinity_amm_v2_decoder::LifinityAmmV2Decoder,
            AggWatch::<carbon_lifinity_amm_v2_decoder::instructions::LifinityAmmV2Instruction>::new("lifinity_amm_v2"),
        )
        .instruction(
            carbon_phoenix_v1_decoder::PhoenixDecoder,
            AggWatch::<carbon_phoenix_v1_decoder::instructions::PhoenixInstruction>::new("phoenix_v1"),
        )
        .instruction(
            carbon_openbook_v2_decoder::OpenbookV2Decoder,
            AggWatch::<carbon_openbook_v2_decoder::instructions::OpenbookV2Instruction>::new("openbook_v2"),
        )
        .instruction(
            carbon_pancake_swap_decoder::PancakeSwapDecoder,
            AggWatch::<carbon_pancake_swap_decoder::instructions::AmmV3Instruction>::new("pancake_swap"),
        )
        .instruction(
            carbon_fluxbeam_decoder::FluxbeamDecoder,
            AggWatch::<carbon_fluxbeam_decoder::instructions::FluxbeamInstruction>::new("fluxbeam"),
        )
        // ── Aggregators — metric-only for now ──
        .instruction(
            carbon_jupiter_swap_decoder::JupiterSwapDecoder,
            AggWatch::<carbon_jupiter_swap_decoder::instructions::JupiterSwapInstruction>::new("jupiter_swap"),
        )
        .instruction(
            carbon_dflow_aggregator_v4_decoder::SwapOrchestratorDecoder,
            AggWatch::<carbon_dflow_aggregator_v4_decoder::instructions::SwapOrchestratorInstruction>::new("dflow_aggregator_v4"),
        )
        .instruction(
            carbon_okx_dex_decoder::OkxDexDecoder,
            AggWatch::<carbon_okx_dex_decoder::instructions::OkxDexInstruction>::new("okx_dex"),
        )
        // ── Context decoders (no triggers; helps Carbon resolve nested ixs) ──
        .instruction(
            carbon_address_lookup_table_decoder::AddressLookupTableDecoder,
            AggWatch::<carbon_address_lookup_table_decoder::instructions::AddressLookupTableInstruction>::new("alt"),
        )
        .instruction(
            carbon_associated_token_account_decoder::SplAssociatedTokenAccountDecoder,
            AggWatch::<carbon_associated_token_account_decoder::instructions::SplAssociatedTokenAccountInstruction>::new("ata"),
        )
        .build()?;

    log::info!("Pipeline built; entering run loop");
    pipeline.run().await?;

    Ok(())
}

fn init_tracing() {
    use tracing_subscriber::{fmt, EnvFilter};
    let filter = EnvFilter::try_from_default_env().unwrap_or_else(|_| "info".into());
    fmt()
        .json()
        .with_target(false)
        .with_env_filter(filter)
        .init();
}

fn spawn_health_server(port: u16) {
    let app = Router::new()
        .route("/health", get(|| async { "ok" }))
        .route("/stats", get(stats_handler));

    tokio::spawn(async move {
        let addr = format!("127.0.0.1:{port}");
        match tokio::net::TcpListener::bind(&addr).await {
            Ok(listener) => {
                log::info!("health endpoint listening on {addr}");
                if let Err(e) = axum::serve(listener, app).await {
                    log::error!("health server exited: {e}");
                }
            }
            Err(e) => {
                log::error!("health endpoint bind {addr} failed: {e}");
            }
        }
    });
}

async fn stats_handler() -> Json<crate::metrics::StatsSnapshot> {
    Json(crate::metrics::snapshot())
}
