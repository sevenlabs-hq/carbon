//! Frankfurt Carbon Shreds — observation-only Carbon decoder pipeline on the
//! local Jito shredstream.
//!
//! Subscribes to `SHRED_GRPC_URL` (typically the in-box shredstream-proxy at
//! 127.0.0.1:9999), registers Carbon's typed decoders for every program we
//! care about, and emits `ObservedTrigger`s for tracked-wallet trades.
//!
//! ── What this binary IS ────────────────────────────────────────────────
//! - A second consumer of the same Jito shred firehose `frankfurt-rust`
//!   already reads. Independent process, independent failure domain.
//! - A typed decoder pipeline using Carbon's autogenerated decoders — 33
//!   PumpFun ix variants vs `decoder.rs`'s 1, all aggregators registered.
//! - A health endpoint at `127.0.0.1:9102` exposing `/health` and `/stats`
//!   (per-program decode counters, watch-list size, miss counters).
//! - A Redis publisher to `shreds_observation:<userId>` channels (no live
//!   consumer reads these yet).
//!
//! ── What this binary IS NOT ────────────────────────────────────────────
//! - It does NOT call any executor.
//! - It does NOT load or touch user keypairs.
//! - It does NOT submit transactions (no QUIC, no RPC sender, no Jito tip).
//! - It does NOT modify any DB tables read by frankfurt-rust or
//!   frankfurt-node engines.
//! - It does NOT write to any channel that frankfurt-rust subscribes to.
//!
//! ── Service contract ───────────────────────────────────────────────────
//! Restarting / crashing / redeploying this binary has zero user-visible
//! effect. frankfurt-rust's hot path (grpc.rs → decoder.rs → executor) is
//! unaffected. This is the same isolation pattern
//! `frankfurt-carbon-surveillance` uses for the surveillance cutover.

